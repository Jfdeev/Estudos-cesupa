public class Livro {
    
    String titulo;
    String autor;
    int preco;
    int quantidadeVenda;

    public Livro(String titulo, String autor, int preco, int quantidadeVenda){
        this.titulo = titulo;
        this.autor = autor;
        this.preco = preco;
        this.quantidadeVenda =quantidadeVenda;
    }

    

}
